ANALYTICS = ANALYTICS + ANALYTICS_PART2


def _assemble() -> list[tuple]:
    return ORIGINALS + CORE + RELATIONAL + NONRELATIONAL + ANALYTICS


def build_bank() -> dict:
    rng = random.Random(900)
    questions = []
    letters = ["A", "B", "C", "D"]

    for idx, entry in enumerate(_assemble(), start=1):
        domain, difficulty, question, correct, d1, d2, d3, explanation = entry
        options = [correct, d1, d2, d3]
        rng.shuffle(options)
        choices = {letters[i]: options[i] for i in range(4)}
        correct_key = letters[options.index(correct)]
        questions.append(
            {
                "id": idx,
                "domain": domain,
                "difficulty": difficulty,
                "origin": "AI generated",
                "question": question,
                "choices": choices,
                "correctAnswer": correct_key,
                "explanation": explanation,
            }
        )

    return {
        "examName": "Microsoft Azure Data Fundamentals (DP-900)",
        "version": "1.0",
        "examSize": 40,
        "totalQuestions": len(questions),
        "domains": DOMAINS,
        "questions": questions,
    }


def main() -> None:
    bank = build_bank()

    # Validate every question has a unique id, 4 choices, and a valid correct key.
    seen_ids = set()
    for q in bank["questions"]:
        assert q["id"] not in seen_ids, f"Duplicate id {q['id']}"
        seen_ids.add(q["id"])
        assert len(q["choices"]) == 4, f"Question {q['id']} must have 4 choices"
        assert q["correctAnswer"] in q["choices"], f"Question {q['id']} has invalid correctAnswer"

    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_FILE.write_text(json.dumps(bank, indent=2), encoding="utf-8")

    counts = {domain: 0 for domain in DOMAINS}
    for q in bank["questions"]:
        counts[q["domain"]] += 1
    print(f"Wrote {bank['totalQuestions']} questions to {OUTPUT_FILE}")
    for domain, count in counts.items():
        print(f"  {domain}: {count}")


if __name__ == "__main__":
    main()
